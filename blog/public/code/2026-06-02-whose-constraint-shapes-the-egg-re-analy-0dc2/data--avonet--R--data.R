#' Avonet bird traits database.
#'
#' AVONET dataset containing comprehensive functional trait data for all birds,
#' including six ecological variables, 11 continuous morphological traits, and
#' information on range size and location. Raw morphological measurements are
#' presented from 90,020 individuals of 11,009 extant bird species sampled from
#' 181 countries. These data are also summarised as species averages in three
#' taxonomic formats, allowing integration with a global phylogeny, geographical
#' range maps, IUCN Red List data and the eBird citizen science database.
#' The AVONET dataset provides the most detailed picture of continuous trait
#' variation for any major radiation of organisms, offering a global template
#' for testing hypotheses and exploring the evolutionary origins, structure and
#' functioning of biodiversity.
#'
#' @format A data frame with 37 variables: \code{Sequence}, \code{Species1},
#' \code{Family1}, \code{Order1}, \code{Avibase.ID1}, \code{Total.individuals},
#' \code{Female}, \code{Male}, \code{Unkown}, \code{Complete.measures},
#' \code{Beak.Length_Culmen}, \code{Beak.Length_Nares}, \code{Beak.Width},
#' \code{Beak.Depth}, \code{Tarsus.Length}, \code{Wing.Length}, \code{Kipps.Distance},
#' \code{Secondary1}, \code{Hand.Wing.Index}, \code{Tail.Length}, \code{Mass},
#' \code{Mass.Source}, \code{Mass.Refs.Other}, \code{Inference}, \code{Traits.inferred},
#' \code{Reference.species}, \code{Habitat}, \code{Habitat.Density}, \code{Migration},
#' \code{Trophic.Level}, \code{Trophic.Niche}, \code{Primary.Lifestyle}, \code{Min.Latitude},
#' \code{Max.Latitude}, \code{Centroid.Latitude}, \code{Centroid.Longitude} and
#' \code{Range.Size}
